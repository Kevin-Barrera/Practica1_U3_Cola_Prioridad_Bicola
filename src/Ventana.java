    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        //BOTON INSERTAR
        TDA_Prioridad dato = new TDA_Prioridad();
        
        dato.setDato(jTextField1.getText().charAt(0));
        dato.setNivel(Integer.parseInt((String)jComboBox1.getSelectedItem()));
        
        boolean respuesta = objBI.insertar(dato);
        String mensaje = "ERROR, COLA BICOLA LLENA POR FIN!";
        
        if(respuesta)mensaje="SE GUARDO CORRECTAMENTE!";
        JOptionPane.showMessageDialog(this,mensaje);
        jTextField1.setText("");
        mostrarCola();
        
    }//GEN-LAST:event_jButton1MouseClicked

