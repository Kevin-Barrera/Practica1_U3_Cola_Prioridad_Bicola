    private void mostrarCola (){
        String cad="";
                
        if(objBI.estaColaVacia()){
            jTextPane1.setText("COLA BICOLA VACIA!!");
            return;
        }
        for(int i=objBI.getIni(); i<=objBI.getFin(); i++){
            
            cad = cad+ "[ "+i+" ] = "+ objBI.getValor(i)+"\n";
        }
        jTextPane1.setText(cad);
    }
